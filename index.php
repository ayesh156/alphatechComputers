var isPlaying = [false, false, false];

window.onscroll = function() {
  for (var i = 0; i < videos.length; i++) {
    var videoPosition = videos[i].getBoundingClientRect();
    if (videoPosition.top < window.innerHeight && videoPosition.bottom >= 0) {
      if (!isPlaying[i]) {
        videos[i].play();
        isPlaying[i] = true;
      }
    } else {
      if (isPlaying[i]) {
        videos[i].pause();
        isPlaying[i] = false;
      }
    }
  }
